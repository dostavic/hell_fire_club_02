const localeIds: Record<Locale, string> = {
  en: 'en-US',
  de: 'de-DE',
  'de-AT': 'de-AT',
  ro: 'ro-RO',
  sk: 'sk-SK',
  cs: 'cs-CZ',
  uk: 'uk-UA',
};

type I18nContextType = {
  language: Locale;
  setLanguage: (lang: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  localeId: string;
};

const I18nContext = createContext<I18nContextType>({
  language: 'en',
  setLanguage: () => {},
  t: (key: string) => key,
  localeId: 'en-US',
});

const detectLanguage = (): Locale => {
  if (typeof window === 'undefined') return 'en';
  const stored = localStorage.getItem('lang') as Locale | null;
  if (stored && translations[stored]) return stored;
  const nav = navigator.language.toLowerCase();
  if (nav.startsWith('de')) return 'de';
  if (nav.startsWith('ro')) return 'ro';
  if (nav.startsWith('sk')) return 'sk';
  if (nav.startsWith('cs')) return 'cs';
  if (nav.startsWith('uk')) return 'uk';
  return 'en';
};

export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [language, setLanguageState] = useState<Locale>(() => detectLanguage());

  const setLanguage = (next: Locale) => {
    setLanguageState(next);
    if (typeof window !== 'undefined') {
      localStorage.setItem('lang', next);
    }
  };

  const translate = (key: string, vars?: Record<string, string | number>) => {
    const template = translations[language]?.[key] ?? translations.en[key] ?? key;
    if (!vars) return template;
    return Object.entries(vars).reduce(
      (acc, [token, value]) => acc.replace(`{${token}}`, String(value)),
      template
    );
  };

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      t: translate,
      localeId: localeIds[language] || localeIds.en,
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);

export const LanguageSwitcher = () => {
  const { language, setLanguage } = useI18n();
  return (
    <select
      value={language}
      onChange={e => setLanguage(e.target.value as Locale)}
      className="border border-slate-300 rounded-md px-3 py-1 text-sm bg-white text-slate-700"
    >
      {LANGUAGE_OPTIONS.map(option => (
        <option key={option.code} value={option.code}>
          {option.label}
        </option>
      ))}
    </select>
  );
};
